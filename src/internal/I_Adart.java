    private void btn_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_deleteMouseClicked
        // TODO add your handling code here:
        int row = TabelAdart.getSelectedRow();
         if(row == -1){
            JOptionPane.showMessageDialog(null, "Pilih data dulu");
            
        }else{
        String kode_adart;
        
        kode_adart = TabelAdart.getValueAt(row, 0).toString();
        
        CmdAdart cAdart = new CmdAdart();
        cAdart.deleteAdart(kode_adart);
        JOptionPane.showMessageDialog(null, "Data terhapus");
        tampilAdart();
         }
    }//GEN-LAST:event_btn_deleteMouseClicked

     public void cari_data(JTable tbl, String text){
        TableRowSorter sorter = new TableRowSorter(tbl.getModel());
        tbl.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
        tbl.setSelectionMode(1);
}
     
      
