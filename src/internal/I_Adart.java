    public void tampilAdart(){
        CmdAdart cAdart = new CmdAdart();
        ArrayList<Adart> myAdart = cAdart.getAllAdart();
        // 4 diperoleh dari banyak kolom
        Object[][] dtAdart = new Object[myAdart.size()][4];
        int mySize = 0;
        for(Adart art : myAdart){
            dtAdart[mySize][0] = art.getKode_adart();
            dtAdart[mySize][1] = art.getPasal();
            dtAdart[mySize][2] = art.getAyat();
            dtAdart[mySize][3] = art.getUraian();
            
            mySize++;
            
        }
        TabelAdart.setModel(new javax.swing.table.DefaultTableModel(
            
                dtAdart,
            new String [] {
                "Kode AD/ART", "Pasal", "Ayat", "Uraian"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });
        jScrollPane2.setViewportView(TabelAdart);
       


    }

